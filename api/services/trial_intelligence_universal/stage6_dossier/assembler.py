def _generate_location_section(trial, stage3):
    """Generate location details"""
    md = "## ðŸ¥ LOCATION DETAILS\n\n"
    
    if stage3:
        matching_locs = stage3.metadata.get('matching_locations') or stage3.metadata.get('nyc_locations', [])
        if matching_locs:
            md += "### Matching Locations\n\n"
            for i, loc in enumerate(matching_locs, 1):
                facility = loc.get('facility', 'Unknown')
                city = loc.get('city', 'Unknown')
                state = loc.get('state', 'Unknown')
                md += f"{i}. **{facility}** - {city}, {state}\n"
        else:
            md += "No matching locations found.\n"
    else:
        md += "Location data not available.\n"
    
    md += "\n---\n\n"
    return md

def _generate_critical_gates(trial, patient):
    """Generate critical gates section"""
    md = "## âš ï¸ CRITICAL GATES\n\n"
    
    eligibility_lower = trial.get('eligibility_text', '').lower()
    critical_gates = patient.get('critical_gates', {})
    
    if 'her2' in eligibility_lower:
        if 'her2_ihc' in critical_gates:
            gate_info = critical_gates['her2_ihc']
            md += f"""### HER2 Testing (Priority: {gate_info.get('priority', 'P1')})
**Status**: {gate_info.get('status', 'PENDING')}  
**Test**: {gate_info.get('test', 'HER2 IHC')}  
**Turnaround**: {gate_info.get('turnaround', '3-5 days')}  
**Cost**: {gate_info.get('cost', 'TBD')}  
**Rationale**: {gate_info.get('rationale', 'HER2 status may be required for enrollment')}

"""
        else:
            md += "### HER2 Testing\n**Status**: PENDING - HER2 testing may be required for enrollment.\n\n"
    
    if 'hrd' in eligibility_lower or 'homologous recombination' in eligibility_lower:
        if 'hrd_testing' in critical_gates:
            gate_info = critical_gates['hrd_testing']
            md += f"""### HRD Testing (Priority: {gate_info.get('priority', 'P1')})
**Status**: {gate_info.get('status', 'PENDING')}  
**Test**: {gate_info.get('test', 'HRD assay')}  
**Turnaround**: {gate_info.get('turnaround', '7-10 days')}  
**Cost**: {gate_info.get('cost', 'TBD')}  
**Rationale**: {gate_info.get('rationale', 'HRD status may be required for enrollment')}

"""
        else:
            md += "### HRD Testing\n**Status**: PENDING - HRD testing may be required for enrollment.\n\n"
    
    if 'her2' not in eligibility_lower and 'hrd' not in eligibility_lower:
        md += "âœ… No critical biomarker gates identified. Proceed with standard eligibility screening.\n"
    
    md += "\n---\n\n"
    return md

